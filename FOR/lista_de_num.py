# Crear una lista de números
mi_lista = [1, 2, 3, 4, 5, 7, 8, 9, 10, 11, 12, 13, 14, 15]

# Añadir un elemento o numero
mi_lista.append(6) # Agrega 6 al final de la lista
print(mi_lista)

# Acceder a un numero o elemento
primer_numero = mi_lista[0]  # Accede al primer numero (1)
print("")
print("el primer numero de la lista es: ", primer_numero)
print("")

# Eliminar un numero o elemento
mi_lista.remove(3) # Elimina el número 3 de la lista
print("lista despues de eliminar el numero o elemento 3:\n",mi_lista)

